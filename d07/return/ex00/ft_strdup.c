#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*duplicate;
	int		i;

	i = 0;
	while (src[i])
		i++;
	if (!(duplicate = (char*)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		duplicate[i] = src[i];
		i++;
	}
	duplicate[i] = '\0';
	return (duplicate);
}
